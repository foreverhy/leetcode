class Solution{
  public:
    int canCompleteCircuit(vector<int>& gas, vector<int> &cost){
        int len = gas.size();
        for (int i = 0; i < len; ++i){
            gas[i] -= cost[i];
        }

        int sum = 0;
        for (int i = 0, j = 1; i < len; i += j){
            sum = 0;
            for (j = 1; j <= len; ++j){
                auto p = (i + j - 1) % len;
                sum += gas[p];
                if (sum < 0){
                    break;
                }
            }
            if (j > len){
                return i;
            }
        }
        return -1;
    }
};



